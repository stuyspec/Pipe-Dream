				<p>You have all found yourself in that moment where you stood patiently on the escalator, frozen in time, contemplating the realities of your life, Stuyvesant, or the world at large. Perhaps you found yourself angered by school policy or inspired by a novel, or had an urge to mutter a long-winded rant under your breath. The Spectator’s Opinions Department can provide a venue for you to share your thoughts on absolutely any subject matter. Gives you the freedom to write anything from a political argument to an account of a personal, intellectual journey. So, go on. Join Opinions and don’t let all those points of view go unwritten.</p>
				<a href="http://stuyspec.com/join/opinions-application/" class="section-email">Apply to Opinions</a>
			</div>
			<div data-section="AE" class="section-description">
				<h3 class="section-title">A&E</h3>
				<p>If you’ve ever read a great book, listened to a breathtaking song, or stopped to take a better look at a striking work of art, you’ve done what we do. As the Spectator’s department of Arts and Entertainment, a lot falls under our jurisdiction. From reviewing the delectable cuisine at events such as the Taste of Tribeca, covering red carpet premiers at the Tribeca Film Festival, and more, our scope is far from narrow. Though we give events in Tribeca priority, our reach spans all over the city. Ultimately, it is our duty to bring the city’s culture to the Stuyvesant community.</p>
				<a href="http://stuyspec.com/join/ae-application/" class="section-email">Apply to A&amp;E</a>
			</div>
			<div data-section="Features" class="section-description">
				<h3 class="section-title">Features</h3>
				<p>The close cousin of News, which covers distinct events, Features covers trends and broader ideas. Profiles of teachers, students, clubs and organizations fall under the umbrella of Features writing, as do investigative reports and coverage of trends in student and faculty opinion. News provides the hard facts, but Features provides the back-story and the analysis, and ultimately aims to give greater insight into the issues that affect Stuyvesant students every day. Writers have great freedom to choose what they want to cover.</p>
				<a href="http://stuyspec.com/join/features-application/" class="section-email">Apply to Features</a>
			</div>
			<div data-section="Humor" class="section-description">
				<h3 class="section-title">Humor</h3>
				<p>The Humor Department specializes in what some would call "fake news" and other, more informed people might call "valuable and indispensable journalism." We are also known as "Slander and Libel," named after the morally questionable means we go about obtaining our quotes. If you are funny, have an affinity for writing, and enjoy publications such as The Onion, the Humor Section is for you.</p>
				<a href="http://stuyspec.com/join/humor-application/" class="section-email">Apply to Humor</a>
			</div>

			<div data-section="copy" class="section-description">
				<h3 class="section-title">Copy</h3>
				<p>Think you are a grammar whiz? The Copy Department may be the place for you! Members get an exclusive first look at all Spectator articles, reading and correcting them just before printing time. Whether you are looking to touch up on SAT vocabulary and grammar, develop your essay skills, or just have a knack for grammar, the Copy Department is sure to improve your proofreading abilities.</p>
				<a href="http://stuyspec.com/join/copy-application/" class="section-email">Apply to Copy</a>
			</div>

