				<p>The Sports Department covers each of the 37 sports teams at Stuyvesant, including news about individual teams and players, as well as new policies that affect Stuyvesant athletics. However, we also sometimes feature the most important things around the professional sports world. The articles we write range from team previews at the beginning of the season, team updates midway through the season, and finally, season recaps. We also write columns, which are opinion pieces on anything to Stuyvesant sports, or even pressing issues in the professional sports world. Finally, for those more interested in the personal level of the athletics scene, we write features on coaches, players or alumni.</p>
